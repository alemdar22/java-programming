package day_29NestedLoopAndArrays;

public class ArraysPractice {
    public static void main(String[] args) {
        int [] nums= new int[2];
        System.out.println(nums[0]); // no value assigned so DEFAULT VALUE is 0(ZERO)

        int [] counters= {1,2,3,4,5};
        System.out.println(counters[0]);

        double [] prices={2.3,4.1,22.0};
        System.out.println(prices[2]);

        String [] cities={"DC","Fairfax","LA","Miami"};
        System.out.println(cities[1]);
    }
}
