package day_24_Loops;

public class WhileLoopReverse {
    public static void main(String[] args) {
        int count =10;
        while(count >=0){
            System.out.println("count "+count);
            count--;
        }
        int unReadEmail= 10;
        while(unReadEmail>=0){
            System.out.println(" Unread emails "+unReadEmail);
            unReadEmail--;
        }
    }
}
