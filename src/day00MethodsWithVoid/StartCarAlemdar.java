package day00MethodsWithVoid;

public class StartCarAlemdar {
    public static void main(String[] args) {
        sitInTheCar();
        startTheCar();
        shiftTheCar();
        driveTheCar();

    }
    public static void sitInTheCar(){
        System.out.println("Open the door");
    }
    public static void startTheCar(){
        System.out.println("press the start button");
    }
    public static void shiftTheCar(){
        System.out.println("Put the gear in speed 1");
    }
    public static void driveTheCar(){
        System.out.println("foot on the gas pedal");
    }
}
