package day11_ComparisonOperators;

public class BooleanComparisonOperators {
    public static void main(String[] args) {
        System.out.println(10 == 10);
        System.out.println(15>10);
        System.out.println(250<300);
        System.out.println(22<=25);
        System.out.println(300>=303);
        System.out.println(35!=29);

        boolean result = 10==10;
        System.out.println(result);
        result = 23 > 28;
        System.out.println(result);
        result= 88 <99;
        System.out.println(result);
        result = 10>=10;
        System.out.println(result);

        String city = "York";
        System.out.println(city=="York");
    }
}
