package day0_Exercises;

public class StringMethods{
    public static void main(String[] args) {
        String name = "inci";
        System.out.println(name.equals("inci"));
        System.out.println(name.equalsIgnoreCase("INci"));
        System.out.println(name.length());
        System.out.println(name.toUpperCase());
        System.out.println(name.startsWith("in"));
        System.out.println(name.equalsIgnoreCase("INC"));

    }
}
